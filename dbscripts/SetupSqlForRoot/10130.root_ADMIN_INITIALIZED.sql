        LOOP
            IF C_ROLE_FIELD.ROLE_ID = C_RULE_FIELD_DENY.ROLE_ID 
                AND C_ROLE_FIELD.FIELD_ID = C_RULE_FIELD_DENY.FIELD_ID THEN
