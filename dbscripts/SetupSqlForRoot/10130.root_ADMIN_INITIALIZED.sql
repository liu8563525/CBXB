                    SELECT LOWER(sys_guid()), 1, 1, I_DOMAIN_ID, 'system', 'system', sysdate, sysdate, GUID, 'RuleFieldDenyAdmin', 'objectId', 'AccessObject', C_RULE_FIELD_DENY.ACCESS_OBJECT_ID FROM DUAL;
            END IF;
        END lOOP;
        END LOOP;
        
END;
