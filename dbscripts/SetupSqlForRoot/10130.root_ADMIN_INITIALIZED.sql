    DELETE FROM CNT_SELECTION WHERE PARENT_ENTITY='RuleFieldDenyAdmin' AND FIELD_ID='objectId' AND DOMAIN_ID = I_DOMAIN_ID;
    DELETE FROM CNT_RULE_FIELD_DENY_ADMIN WHERE DOMAIN_ID = I_DOMAIN_ID;
        
